def run_verify_8_claims(noise_frac: float, seed: int) -> dict:
    """Run verify_8_claims.py and parse results."""
    import subprocess

    script = PROJECT_ROOT / "scripts" / "verify_8_claims.py"
    cmd = [
        sys.executable, str(script),
        "--honest-data",
        "--noise", str(noise_frac),
        "--seed", str(seed),
    ]

    print(f"\n{'=' * 60}")
    print("Running verify_8_claims.py (math-trained generators)")
    print(f"  Command: {' '.join(cmd)}")
    print(f"{'=' * 60}")

    env = os.environ.copy()
    env["PYTHONUNBUFFERED"] = "1"

    result = subprocess.run(
        cmd,
        capture_output=True,
        text=True,
        cwd=str(PROJECT_ROOT),
        env=env,
        timeout=600,  # 10 min
    )

    stdout = result.stdout
    stderr = result.stderr

    print(stdout[-2000:] if len(stdout) > 2000 else stdout)
    if stderr:
        print(f"STDERR: {stderr[-500:]}")

    # Parse results
    passed = stdout.count("PASS")
    failed = stdout.count("FAIL")
    total = passed + failed

    return {
        "passed": passed,
        "failed": failed,
        "total": total,
        "stdout_tail": stdout[-3000:] if len(stdout) > 3000 else stdout,
        "stderr_tail": stderr[-500:] if stderr else "",
        "returncode": result.returncode,
    }


