# ── Math-specific symbol pool ──────────────────────────────────────────────
# Maps math identity variables to vocab-compatible symbols.
# All symbols must be in QUANTITY_VOCAB / TEMPLATE_TOKENS.
# We use a fixed pool of 52 symbols (enough for complex identities like
# det product with 8 variables) and cycle through them.
_MATH_SYMBOL_POOL = [
    # Single letters (a-z all in vocab)
    "a", "b", "c", "d", "e", "f", "g", "h", "i", "j",
    "k", "l", "m", "n", "o", "p", "q", "r", "s", "t",
    "u", "v", "w", "x", "y", "z",
    # Multi-letter fallbacks
    "a0", "a1", "b0", "b1", "c0", "c1", "d0", "d1",
    "e0", "e1", "f0", "f1", "g0", "g1", "h0", "h1",
    "v1", "v2", "x1", "x2", "m1", "m2", "q1", "q2",
    "L", "B", "W", "Q",
]


def _remap_symbols_for_invariant(inv) -> dict[str, str]:
    """Create a mapping from original math variable names to vocab symbols."""
    # The MathInvariant has a list of variable names generated by _next_symbol().
    # We remap them to our pool so they're all in QUANTITY_VOCAB.
    mapping: dict[str, str] = {}
    pool_idx = 0
    for orig_var in inv.variables:
        # First check if it's already a valid token
        from src.physics.composer import TEMPLATE_TOKEN_TO_ID
        if orig_var in TEMPLATE_TOKEN_TO_ID:
            mapping[orig_var] = orig_var
        else:
            # Use next pool symbol
            mapping[orig_var] = _MATH_SYMBOL_POOL[pool_idx % len(_MATH_SYMBOL_POOL)]
            pool_idx += 1
    return mapping


def _remap_expression(expr: str, mapping: dict[str, str]) -> str:
    """Remap variable names in an expression string."""
    result = expr
    # Sort by length (longest first) to avoid partial matches
    for orig, new in sorted(mapping.items(), key=lambda x: -len(x[0])):
        result = result.replace(orig, new)
    return result


# ── Math identity -> training example ──────────────────────────────────────

def identity_to_example(inv, noise_frac: float, rng: random.Random,
                        n_observations: int = 10) -> dict[str, Any] | None:
    """Convert a MathInvariant to a template training example.

    Each example has:
      - quantities: list of variable symbols (input to template generator)
      - domain: identity type (algebraic, trigonometric, etc.)
      - expression: the invariant expression (training target)
      - complexity: 1-5
      - noisy_observations: list of noisy value samples (for verification)

    Returns None if the identity can't be mapped (symbol not in vocab).
    """
    from src.physics.composer import TEMPLATE_TOKEN_TO_ID

    # Remap variables to vocab-compatible symbols
    mapping = _remap_symbols_for_invariant(inv)

    # Check all mapped symbols are valid
    for orig, new in mapping.items():
        if new not in TEMPLATE_TOKEN_TO_ID:
            # This shouldn't happen with our pool, but guard against it
            return None

    # Remap expression
    expr_remapped = _remap_expression(inv.expression, mapping)

    # Check expression is tokenizable
    try:
        from src.physics.composer import tokenize_expression, TEMPLATE_UNK_IDX
        tokens = tokenize_expression(expr_remapped)
        # Allow some UNK tokens (single-char variables that happen to not match)
        # but reject if too many unknowns
        unk_ratio = sum(1 for t in tokens if t == TEMPLATE_UNK_IDX) / max(len(tokens), 1)
        if unk_ratio > 0.3:
            return None
    except Exception:
        return None

    # Remap variables list
    var_list = sorted(set(mapping.values()))

    # Generate noisy observations
    try:
        raw_values = inv.generate_values(n_observations, rng)
    except Exception:
        return None

    noisy_observations = []
    for raw in raw_values:
        obs = {}
        for orig_var, orig_val in raw.items():
            new_var = mapping.get(orig_var, orig_var)
            # Add Gaussian noise to simulate measurement error
            obs[new_var] = orig_val * rng.gauss(1.0, noise_frac)
        noisy_observations.append(obs)

    return {
        "quantities": var_list,
        "domain": inv.identity_type,
        "expression": expr_remapped,
        "complexity": inv.complexity,
        "description": inv.description,
        "noisy_observations": noisy_observations,
    }


# ── Data generation ────────────────────────────────────────────────────────

def generate_math_training_data(
    n_identities: int = 100_000,
    noise_frac: float = 0.02,
    seed: int = 42,
    output_path: str | Path = "data/math_self_play_training.jsonl",
) -> tuple[list[dict], int]:
    """Generate math identity training data.

    Returns:
        (list of training examples, number of identities attempted)
    """
    from src.math import MathInvariantGenerator, reset_symbol_pool

    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    rng = random.Random(seed)
    gen = MathInvariantGenerator(seed=seed)

    # Track stats
    type_counts: dict[str, int] = defaultdict(int)
    examples: list[dict] = []
    attempts = 0
    max_attempts = n_identities * 3  # safety limit

    print(f"Generating up to {n_identities} math identities...")
    print(f"Noise fraction: {noise_frac*100:.0f}%")
    print(f"Available invariant types: {gen.available_types}")

    # Balance across all types
    all_types = gen.available_types

    while len(examples) < n_identities and attempts < max_attempts:
        attempts += 1

        # Cycle through all types for balanced coverage
        inv_type = all_types[attempts % len(all_types)]
        try:
            inv = gen.generate(type_filter=inv_type)
        except Exception:
            continue

        example = identity_to_example(inv, noise_frac, rng)
        if example is None:
            continue

        examples.append(example)
        type_counts[inv.identity_type] += 1

        if len(examples) % 5000 == 0:
            print(f"  Generated {len(examples):,} / {n_identities:,} "
                  f"(attempts: {attempts:,})")

    # Write JSONL
    with open(output_path, "w") as f:
        for ex in examples:
            json.dump(ex, f)
            f.write("\n")

    print(f"\nGenerated {len(examples):,} training examples "
          f"(from {attempts:,} attempts)")
    print(f"Type distribution:")
    for t, c in sorted(type_counts.items()):
        print(f"  {t:30s}: {c:6,}")

    return examples, attempts


# ── Math template dataset ──────────────────────────────────────────────────

class MathTemplateDataset(torch.utils.data.Dataset):
    """Dataset for training template generator from math identities.

    Input: variable symbols → Output: expression string.
    Domain-agnostic: we train ONE model on all identity types.
    """

    def __init__(
        self,
        examples: list[dict],
        src_max_len: int = 10,
        tgt_max_len: int = 48,
    ):
        from src.physics.composer import (
            quantities_to_tensor, expression_to_tensor,
            TEMPLATE_TOKEN_TO_ID, TEMPLATE_UNK_IDX,
        )

        self.samples: list[tuple[torch.Tensor, torch.Tensor]] = []

        for ex in examples:
            qty_symbols = ex.get("quantities", [])
            # Filter to only valid vocab symbols
            qty_symbols = [q for q in qty_symbols if q in TEMPLATE_TOKEN_TO_ID]
            if len(qty_symbols) < 1:
                continue

            expr = ex.get("expression", "")
            if not expr or not expr.strip():
                continue

            # Sort for consistency
            qty_sorted = sorted(qty_symbols)
            src = quantities_to_tensor(qty_sorted, max_len=src_max_len)

            # Target expression
            tgt = expression_to_tensor(expr, max_len=tgt_max_len)

            self.samples.append((src, tgt))

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, torch.Tensor]:
        return self.samples[idx]


# ── Training ───────────────────────────────────────────────────────────────

def train_math_template_generator(
    examples: list[dict],
    checkpoint_path: str | Path,
    epochs: int = 50,
    lr: float = 1e-3,
    batch_size: int = 64,
    device: torch.device = torch.device("cpu"),
    d_model: int = 64,
    nhead: int = 4,
) -> tuple[nn.Module, dict]:
    """Train one general math template generator.

    Uses a slightly larger model (64-dim, 4-head) than domain-specific ones
    (48-dim, 2-head) because it must cover all identity types.
    """
    from src.physics.composer import (
        DomainTemplateGenerator,
        TEMPLATE_PAD_IDX, TEMPLATE_VOCAB_SIZE,
    )

    print(f"\n{'=' * 60}")
    print(f"Training Math Template Generator")
    print(f"{'=' * 60}")

    dataset = MathTemplateDataset(examples, src_max_len=10, tgt_max_len=48)
    n_samples = len(dataset)
    print(f"  Training examples: {n_samples:,}")

    if n_samples == 0:
        raise ValueError("No valid training examples")

    dataloader = DataLoader(
        dataset,
        batch_size=min(batch_size, n_samples),
        shuffle=True,
        drop_last=True,
    )

    model = DomainTemplateGenerator(
        d_model=d_model,
        nhead=nhead,
        max_src_len=10,
        max_tgt_len=48,
        vocab_size=TEMPLATE_VOCAB_SIZE,
    )
    n_params = model.count_parameters()
    print(f"  Parameters: {n_params:,}")
    print(f"  Vocab size: {TEMPLATE_VOCAB_SIZE}")
    print(f"  d_model={d_model}, nhead={nhead}")

    criterion = nn.CrossEntropyLoss(ignore_index=TEMPLATE_PAD_IDX)
    optimizer = torch.optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-5)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)

    stats: dict[str, list[float]] = {
        "train_loss": [], "train_acc": [], "epoch_time": [],
    }

    model.train()
    model.to(device)

    best_loss = float("inf")

    for epoch in range(1, epochs + 1):
        t_start = time.time()
        epoch_loss = 0.0
        epoch_correct = 0
        epoch_total = 0
        n_batches = 0

        for src, tgt in dataloader:
            src = src.to(device)
            tgt = tgt.to(device)

            optimizer.zero_grad()

            # Teacher forcing
            tgt_input = tgt[:, :-1]
            tgt_output = tgt[:, 1:]

            logits = model(src, tgt_input)
            # logits: [batch, tgt_len-1, vocab_size]
            loss = criterion(
                logits.reshape(-1, logits.size(-1)),
                tgt_output.reshape(-1),
            )
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()

            epoch_loss += loss.item()
            n_batches += 1

            # Accuracy (teacher-forced)
            preds = logits.argmax(dim=-1)
            mask = tgt_output != TEMPLATE_PAD_IDX
            epoch_correct += (preds == tgt_output)[mask].sum().item()
            epoch_total += mask.sum().item()

        scheduler.step()

        avg_loss = epoch_loss / n_batches
        avg_acc = epoch_correct / max(epoch_total, 1)
        epoch_time = time.time() - t_start

        stats["train_loss"].append(avg_loss)
        stats["train_acc"].append(avg_acc)
        stats["epoch_time"].append(epoch_time)

        if avg_loss < best_loss:
            best_loss = avg_loss
            # Save best checkpoint
            from src.physics.composer import save_domain_generator
            save_domain_generator(model, str(checkpoint_path))
            saved = True
        else:
            saved = False

        marker = " *" if saved else ""
        print(f"  Epoch {epoch:3d}/{epochs} | loss={avg_loss:.4f} "
              f"acc={avg_acc:.3f} time={epoch_time:.1f}s{marker}")

    # Load best
    from src.physics.composer import load_domain_generator
    model = load_domain_generator(str(checkpoint_path))
    model.eval()

    print(f"\n  Final checkpoint → {checkpoint_path}")
    print(f"  Best loss: {best_loss:.4f}")
    return model, stats


# ── Clone to domain slots ─────────────────────────────────────────────────

def clone_to_domain_slots(
    math_ckpt_path: str | Path,
    domains: list[str],
    checkpoint_dir: str | Path = "checkpoints",
) -> list[str]:
    """Clone math template generator to physics domain slots.

    Saves copies as self_play_{domain}_template.pt for each domain.
    """
    import shutil

    checkpoint_dir = Path(checkpoint_dir)
    saved: list[str] = []

    math_ckpt = Path(math_ckpt_path)
    if not math_ckpt.exists():
        print(f"ERROR: Math checkpoint not found at {math_ckpt}")
        return saved

    for domain in domains:
        dest = checkpoint_dir / f"self_play_{domain}_template.pt"
        # Backup existing
        if dest.exists():
            backup = checkpoint_dir / f"self_play_{domain}_template_backup.pt"
            if not backup.exists():
                shutil.copy(dest, backup)
                print(f"  Backed up existing → {backup}")
        shutil.copy(math_ckpt, dest)
        saved.append(str(dest))
        print(f"  Cloned → {dest}")

    return saved


