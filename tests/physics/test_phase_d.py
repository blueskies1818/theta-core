class TestSelfPlayData:
    """Verify training data collection produces valid triples."""

    @pytest.fixture
    def generator(self):
        return SelfPlayExpressionGenerator(seed=42, include_hidden_vars=False)

    def test_generator_produces_pre1905_only(self, generator):
        """All generated expressions use only pre-1905 symbols."""
        for level in [1, 2, 3]:
            for _ in range(20):
                result = generator.generate(level)
                qty_symbols = set(result.quantities_dict.keys())
                for q in qty_symbols:
                    assert q in PRE_1905_QUANTITY_DIMS, (
                        f"Post-1905 symbol '{q}' in level {level}: {result.expression_str}"
                    )

    def test_all_expressions_parseable(self, generator):
        """Every generated expression should be parseable."""
        from src.physics.evaluator import ExpressionEvaluator
        evaluator = ExpressionEvaluator()
        for level in [1, 2, 3]:
            for _ in range(10):
                result = generator.generate(level)
                try:
                    evaluator.parse(result.expression_str)
                except Exception as e:
                    pytest.fail(
                        f"Expression '{result.expression_str}' "
                        f"(level {level}) fails to parse: {e}"
                    )

    def test_domain_label_is_valid(self, generator):
        """Domain label should be one of the expected domains."""
        valid_domains = {"gravity", "spring", "thermal", "em",
                         "classical", "mechanics", "gas_law"}
