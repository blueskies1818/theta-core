class TestTemplateGeneratorTraining:
    """Verify template generator can train on self-play data."""

    def test_model_initialization(self) -> None:
        model = DomainTemplateGenerator(d_model=48, nhead=2)
        n_params = model.count_parameters()
        assert 20_000 < n_params < 200_000, (
            f"Expected moderate params, got {n_params}"
        )

    def test_gradient_flow(self) -> None:
        """Verify gradients flow through fresh model."""
        model = DomainTemplateGenerator(d_model=48, nhead=2)
        model.train()

        src = quantities_to_tensor(["m", "g", "h", "v"], max_len=8).unsqueeze(0)
        tgt = quantities_to_tensor(["m", "g", "h"], max_len=32).unsqueeze(0)
        tgt_input = tgt[:, :-1]

        logits = model.forward(src, tgt_input)
        loss = logits.sum()
        loss.backward()

        has_grad = any(
            p.grad is not None and p.grad.abs().sum() > 0
            for p in model.parameters()
        )
        assert has_grad, "No gradients flowing"

    def test_save_load_roundtrip(self, tmp_path) -> None:
        """Save and load should preserve weights."""
        model = DomainTemplateGenerator(d_model=48, nhead=2)
        path = tmp_path / "test_gen.pt"
        save_domain_generator(model, str(path))

        loaded = load_self_play_generators(str(tmp_path))
        # Auto-detect won't find it (wrong prefix), so test manually
        from src.physics.composer import load_domain_generator
        loaded2 = load_domain_generator(str(path))
        assert loaded2.count_parameters() == model.count_parameters()

    def test_training_converges_on_synthetic_data(self, tmp_path) -> None:
        """Model should converge on simple synthetic data."""
        import torch.nn as nn
        from torch.utils.data import DataLoader, TensorDataset

        from src.physics.composer import expression_to_tensor

        # Create 100 synthetic examples: {m,g,h} → m*g*h
        src_list: list[torch.Tensor] = []
        tgt_list: list[torch.Tensor] = []
        for _ in range(100):
            src_list.append(quantities_to_tensor(["m", "g", "h"], max_len=8))
            tgt_list.append(expression_to_tensor("m*g*h", max_len=16))

        src_batch = torch.stack(src_list)
        tgt_batch = torch.stack(tgt_list)
        dataset = TensorDataset(src_batch, tgt_batch)
        loader = DataLoader(dataset, batch_size=8, shuffle=True)

        model = DomainTemplateGenerator(d_model=32, nhead=2, max_tgt_len=16)
        model.train()
        optimizer = torch.optim.Adam(model.parameters(), lr=5e-3)
        criterion = nn.CrossEntropyLoss(ignore_index=TEMPLATE_PAD_IDX)

        losses: list[float] = []
        for epoch in range(50):
            epoch_loss = 0.0
            for src, tgt in loader:
                optimizer.zero_grad()
                tgt_input = tgt[:, :-1]
                tgt_output = tgt[:, 1:]
                logits = model.forward(src, tgt_input)
                loss = criterion(
                    logits.reshape(-1, logits.size(-1)),
                    tgt_output.reshape(-1),
                )
                loss.backward()
                optimizer.step()
                epoch_loss += loss.item()
            losses.append(epoch_loss / len(loader))

        # Loss should decrease significantly
        assert losses[-1] < losses[0] * 0.3, (
            f"Training did not converge: loss went from {losses[0]:.4f} "
            f"to {losses[-1]:.4f}"
        )

        # Generate should produce the learned template
        model.eval()
        src = quantities_to_tensor(["m", "g", "h"], max_len=8).unsqueeze(0)
        gen_ids = model.generate(src, max_len=16)
        generated = detokenize_expression(gen_ids[0])
        assert "m" in generated and "g" in generated and "h" in generated, (
            f"Generation '{generated}' doesn't contain expected quantities"
        )


