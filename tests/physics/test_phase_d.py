# Test 1: No post-1905 hardcoded templates
# ═══════════════════════════════════════════════════════════════════════════════

class TestNoPost1905Templates:
    """Verify that DOMAIN_TEMPLATES only contains pre-1905 classical domains."""

    def test_quantum_removed(self) -> None:
        assert "quantum" not in DOMAIN_TEMPLATES, (
            "Quantum template should be removed from DOMAIN_TEMPLATES. "
            "It must be generated by a trained model."
        )

    def test_relativistic_removed(self) -> None:
        assert "relativistic" not in DOMAIN_TEMPLATES, (
            "Relativistic template should be removed from DOMAIN_TEMPLATES. "
            "It must be generated by a trained model."
        )

    def test_pre1905_domains_present(self) -> None:
        assert "gravity" in DOMAIN_TEMPLATES
        assert "spring" in DOMAIN_TEMPLATES
        assert "em" in DOMAIN_TEMPLATES
        assert "thermal" in DOMAIN_TEMPLATES
        assert "collision" in DOMAIN_TEMPLATES

    def test_no_post1905_symbols_in_templates(self) -> None:
        """No post-1905 symbols (hbar, c, p, gamma) in any template."""
        post1905 = {"hbar", "c", "p", "gamma"}
        for domain, template in DOMAIN_TEMPLATES.items():
            for sym in post1905:
                assert sym not in template, (
                    f"Post-1905 symbol '{sym}' found in {domain} template: {template}"
                )

    def test_templates_are_valid_expressions(self) -> None:
        """Template strings should be parseable."""
        from src.physics.evaluator import ExpressionEvaluator
        evaluator = ExpressionEvaluator()
        for domain, template in DOMAIN_TEMPLATES.items():
            try:
                evaluator.parse(template)
            except Exception as e:
                pytest.fail(f"Template for '{domain}' fails to parse: {e}")


