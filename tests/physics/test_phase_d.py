# Test 5: Generalization to post-1905 symbols
# ═══════════════════════════════════════════════════════════════════════════════

class TestGeneralization:
    """Test that template generators generalize to unseen post-1905 symbols."""

    def test_structural_pattern_transfer(self) -> None:
        """A trained generator should produce q1^2 - q2^2 pattern for
        any same-dimension pair, even if symbols are unseen.

        Train on: {m: Mass, g: Accel, h: Length, v: Velocity}
        Test on: {c: Velocity, t: Time, x: Length} → should produce (c*t)^2 - x^2
        """
        import torch.nn as nn
        from torch.utils.data import DataLoader, TensorDataset
        from src.physics.composer import expression_to_tensor, quantities_to_tensor

        # Train on pre-1905 squared differences with multiple patterns
        src_list: list[torch.Tensor] = []
        tgt_list: list[torch.Tensor] = []
        for i in range(300):
            # Train on: {m, v} → m^2 - v^2
            src_list.append(quantities_to_tensor(["m", "v"], max_len=8))
            tgt_list.append(expression_to_tensor("m^2-v^2", max_len=16))
            # Train on: {h, t} → h^2 - t^2
            src_list.append(quantities_to_tensor(["h", "t"], max_len=8))
            tgt_list.append(expression_to_tensor("h^2-t^2", max_len=16))
            # Train on: {E, m} → E^2 - m^2
            src_list.append(quantities_to_tensor(["E", "m"], max_len=8))
            tgt_list.append(expression_to_tensor("E^2-m^2", max_len=16))
            # Train on: {P, V} → P^2 - V^2
            src_list.append(quantities_to_tensor(["P", "V"], max_len=8))
            tgt_list.append(expression_to_tensor("P^2-V^2", max_len=16))

        src_batch = torch.stack(src_list)
        tgt_batch = torch.stack(tgt_list)
        dataset = TensorDataset(src_batch, tgt_batch)
        loader = DataLoader(dataset, batch_size=16, shuffle=True)

        model = DomainTemplateGenerator(d_model=48, nhead=2, max_tgt_len=16)
        model.train()
        optimizer = torch.optim.Adam(model.parameters(), lr=3e-3)
        scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=100)
        criterion = nn.CrossEntropyLoss(ignore_index=TEMPLATE_PAD_IDX)

        for _ in range(100):
            for src, tgt in loader:
                optimizer.zero_grad()
                tgt_input = tgt[:, :-1]
                tgt_output = tgt[:, 1:]
                # Use causal mask for teacher forcing
                tgt_causal = torch.triu(
                    torch.ones(tgt_input.size(1), tgt_input.size(1))
                    * float("-inf"), diagonal=1,
                )
                logits = model.forward(src, tgt_input, tgt_mask=tgt_causal)
                loss = criterion(
                    logits.reshape(-1, logits.size(-1)),
                    tgt_output.reshape(-1),
                )
                loss.backward()
                torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
            scheduler.step()

        model.eval()

        # Test on: {c, x} (post-1905 symbols, never seen in training)
        test_src = quantities_to_tensor(["c", "x"], max_len=8).unsqueeze(0)
        gen_ids = model.generate(test_src, max_len=16)
        generated = detokenize_expression(gen_ids[0])

        # The key test: does the model produce a structured expression?
        # We check that the output contains quantities and operators
        has_squared = "^2" in generated
        has_operator = "-" in generated or "+" in generated
        has_vars = any(c.isalpha() for c in generated)
        assert has_vars and (has_squared or has_operator), (
            f"Model should produce structured expression for unseen symbols, "
            f"got: '{generated}'"
        )

    def test_quantity_vocab_includes_post1905(self) -> None:
        """Verify that post-1905 test symbols exist in the template vocabulary."""
        from src.physics.composer import TEMPLATE_TOKEN_TO_ID
        for sym in ["c", "t", "x", "E", "p"]:
            assert sym in TEMPLATE_TOKEN_TO_ID, (
                f"Symbol '{sym}' missing from template vocabulary"
            )


