class TestFallbackBehavior:
    """Verify fallback to DOMAIN_TEMPLATES when generator is untrained."""

    def test_untrained_generator_falls_back(self) -> None:
        """PerDomainComposer with untrained generators should use hardcoded templates."""
        from src.physics.composer import DomainClassifier

        clf = DomainClassifier()
        generators = {
            "gravity": DomainTemplateGenerator(d_model=40, nhead=2),
            "spring": DomainTemplateGenerator(d_model=40, nhead=2),
        }
        composer = PerDomainComposer(clf, generators)

        expr, domains = composer.forward(["m", "g", "h", "v"])
        # Even with untrained generators, forward should not crash
        assert isinstance(expr, str)
        assert isinstance(domains, list)

    def test_fallback_when_no_generator(self) -> None:
        """When domain has no generator, DOMAIN_TEMPLATES should be used."""
        from src.physics.composer import DomainClassifier

        clf = DomainClassifier()
        # Only gravity generator, no spring
        generators = {"gravity": DomainTemplateGenerator(d_model=40, nhead=2)}
        composer = PerDomainComposer(clf, generators)

        expr, domains = composer.forward(["m", "k", "h", "v"])
        assert isinstance(expr, str)
        # Should not crash even without a spring generator


