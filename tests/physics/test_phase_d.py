    def test_pipeline_with_trained_generators(self, tmp_path) -> None:
        """Pipeline should prefer trained generator output over hardcoded templates."""
        import torch.nn as nn
        from torch.utils.data import DataLoader, TensorDataset
        from src.physics.composer import expression_to_tensor

        # Train a gravity generator to output a specific template
        src_list = []
        tgt_list = []
        for _ in range(100):
            src_list.append(quantities_to_tensor(["m", "g", "h", "v"], max_len=8))
            tgt_list.append(expression_to_tensor("m*g*h + 0.5*m*v^2", max_len=32))

        src_batch = torch.stack(src_list)
        tgt_batch = torch.stack(tgt_list)
        dataset = TensorDataset(src_batch, tgt_batch)
        loader = DataLoader(dataset, batch_size=8, shuffle=True)

        model = DomainTemplateGenerator(d_model=32, nhead=2)
        model.train()
        optimizer = torch.optim.Adam(model.parameters(), lr=5e-3)
        criterion = nn.CrossEntropyLoss(ignore_index=TEMPLATE_PAD_IDX)

        for _ in range(50):
            for src, tgt in loader:
                optimizer.zero_grad()
                tgt_input = tgt[:, :-1]
                tgt_output = tgt[:, 1:]
                logits = model.forward(src, tgt_input)
                loss = criterion(
                    logits.reshape(-1, logits.size(-1)),
                    tgt_output.reshape(-1),
                )
                loss.backward()
                optimizer.step()

        model.eval()

        # Save trained generator
        ckpt_dir = Path(tmp_path)
        save_domain_generator(model, str(ckpt_dir / "self_play_gravity_template.pt"))

        # Load composer
        composer = load_self_play_composer(str(ckpt_dir))
        expr, domains = composer.forward(["m", "g", "h", "v"])

        # The generated expression should contain the expected quantities
        assert "m" in expr, f"Expression '{expr}' missing 'm'"
        assert "g" in expr, f"Expression '{expr}' missing 'g'"
